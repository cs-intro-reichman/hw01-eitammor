// The program add two given integers and prints the result.
public class AddTwo {
	public static void main(String[] args) {
		// Set two variables according to the given numbers
		int a = Integer.parseInt ( args[0] );
		int b = Integer.parseInt ( args[1] );
		//Prints the result of adding the two given numbers.
		System.out.println ( a + " + " + b + " = " + (a+b));
	}
}
		